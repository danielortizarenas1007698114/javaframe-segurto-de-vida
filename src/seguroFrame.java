    private void calcularSeguro(){
        valorBase = Long.parseLong(textvalorbase.getText());
        if(rbfumadorsi.isSelected()){
            fumador = true;
        }else{
            fumador = false;
        }if (rbalcoholsi.isSelected()){
            alcholico = true;
        }else{
            alcholico = false;
        }if (rbalucinogenassi.isSelected()){
            drogadicto = true;
        }else{
            drogadicto = false;
        }if (rbcardiacosi.isSelected()){
            cardiaco = true;
        }else{
            cardiaco = false; 
        }
        labelfinal.setText(Long.toString(calcular.calcular(valorBase, fumador, alcholico, drogadicto, cardiaco)));
            
    }
    
